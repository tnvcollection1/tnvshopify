@router.get("/")
async def list_themes():
    """
    List all themes in the Shopify store.
    Returns published and unpublished themes.
    """
    if not SHOPIFY_SHOP_URL or not SHOPIFY_ACCESS_TOKEN:
        raise HTTPException(status_code=400, detail="Shopify credentials not configured")
    
    try:
        async with httpx.AsyncClient(timeout=30.0) as client:
            response = await client.get(
                get_shopify_url("themes.json"),
                headers=get_shopify_headers()
            )
            
            if response.status_code != 200:
                raise HTTPException(status_code=response.status_code, detail=response.text)
            
            data = response.json()
            themes = data.get("themes", [])
            
            return {
                "success": True,
                "themes": [
                    {
                        "id": t.get("id"),
                        "name": t.get("name"),
                        "role": t.get("role"),  # main = published, unpublished, demo
                        "theme_store_id": t.get("theme_store_id"),
                        "previewable": t.get("previewable"),
                        "created_at": t.get("created_at"),
                        "updated_at": t.get("updated_at"),
                    }
                    for t in themes
                ],
                "published_theme": next((t for t in themes if t.get("role") == "main"), None)
            }
            
    except httpx.TimeoutException:
        raise HTTPException(status_code=504, detail="Shopify API timeout")
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


@router.get("/{theme_id}/assets")
async def list_theme_assets(
    theme_id: int,
    filter_type: Optional[str] = Query(None, description="Filter by type: liquid, css, js, json")
):
    """
    List all assets in a theme.
    """
    if not SHOPIFY_SHOP_URL or not SHOPIFY_ACCESS_TOKEN:
        raise HTTPException(status_code=400, detail="Shopify credentials not configured")
    
    try:
        async with httpx.AsyncClient(timeout=30.0) as client:
            response = await client.get(
                get_shopify_url(f"themes/{theme_id}/assets.json"),
                headers=get_shopify_headers()
            )
            
            if response.status_code != 200:
                raise HTTPException(status_code=response.status_code, detail=response.text)
            
            data = response.json()
            assets = data.get("assets", [])
            
            # Filter by type if specified
            if filter_type:
                assets = [a for a in assets if a.get("key", "").endswith(f".{filter_type}")]
            
            # Group by folder
            grouped = {}
            for asset in assets:
                key = asset.get("key", "")
                folder = key.split("/")[0] if "/" in key else "root"
                if folder not in grouped:
                    grouped[folder] = []
                grouped[folder].append({
                    "key": key,
                    "content_type": asset.get("content_type"),
                    "size": asset.get("size"),
                    "updated_at": asset.get("updated_at"),
                })
            
            return {
                "success": True,
                "theme_id": theme_id,
                "total_assets": len(assets),
                "assets_by_folder": grouped,
                "price_related_files": [
                    a for a in assets 
                    if any(term in a.get("key", "").lower() for term in ["price", "product", "card", "badge"])
                ]
            }
            
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


