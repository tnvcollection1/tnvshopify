"""
Shopify Theme API Integration
Manage theme files and add discount percentage badges automatically

API: Shopify Admin API - Themes
"""

from fastapi import APIRouter, HTTPException, Query, Body
from pydantic import BaseModel, Field
from typing import Optional, List, Dict, Any
import httpx
import os
import re

router = APIRouter(prefix="/api/shopify/themes", tags=["Shopify Themes"])

# Shopify Configuration
SHOPIFY_SHOP_URL = os.environ.get("SHOPIFY_SHOP_URL", "").replace("https://", "").replace("http://", "").rstrip("/")
SHOPIFY_ACCESS_TOKEN = os.environ.get("SHOPIFY_ACCESS_TOKEN", "")
SHOPIFY_API_VERSION = "2024-01"


def get_shopify_headers():
    return {
        "X-Shopify-Access-Token": SHOPIFY_ACCESS_TOKEN,
        "Content-Type": "application/json",
    }


def get_shopify_url(endpoint: str) -> str:
    return f"https://{SHOPIFY_SHOP_URL}/admin/api/{SHOPIFY_API_VERSION}/{endpoint}"


# =============================================================================
# MODELS
# =============================================================================

class UpdateAssetRequest(BaseModel):
    theme_id: int = Field(..., description="Theme ID")
    asset_key: str = Field(..., description="Asset key (e.g., 'snippets/price.liquid')")
    value: str = Field(..., description="New content for the asset")


class AddDiscountBadgeRequest(BaseModel):
    theme_id: int = Field(..., description="Theme ID to update")
    badge_style: str = Field("red", description="Badge color: red, green, black, orange")
    badge_position: str = Field("top-left", description="Position: top-left, top-right, bottom-left, bottom-right")


